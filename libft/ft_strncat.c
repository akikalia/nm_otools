#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t m)
{
	int		i;
	size_t	n;

	n = 0;
	i = 0;
	while (s1[i] != '\0')
		i++;
	while (s2[n] != '\0' && n < m)
	{
		s1[i + n] = s2[n];
		n++;
	}
	s1[i + n] = '\0';
	return (s1);
}
